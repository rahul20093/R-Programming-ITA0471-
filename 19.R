# Create a vector
vec <- c(1, 2, 3, 4, 5)

# Create a matrix
mat <- matrix(c(1, 2, 3, 4, 5, 6, 7, 8, 9), nrow = 3, ncol = 3)

# Define a function
my_function <- function(x) {
  return(x ^ 2)
}

# Create a list
my_list <- list(vector = vec, matrix = mat, my_function)

# Print the content of the list
print(my_list)