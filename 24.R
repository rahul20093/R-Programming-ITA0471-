# Generate a sequence of even integers greater than 50
even_integers <- seq(52, by = 2, length.out = 15)

# Create a 5x3 array
array_5x3 <- matrix(even_integers, nrow = 5, ncol = 3, byrow = TRUE)

# Print the array
print("The created 5x3 array of even integers greater than 50:")
print(array_5x3)
